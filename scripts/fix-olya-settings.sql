-- Скрипт для исправления настроек Оли (ID: 476561547) в основном боте
--
-- ПРОБЛЕМА:
-- Оля использует один chat_id (476561547) в обоих ботах (тестовом и основном)
-- В БД продакшн сервера хранятся настройки из ТЕСТОВОГО бота:
--   dm_enabled=0, channel_enabled=1, channel_id=-1002846400650
--
-- Когда Оля регистрируется в ОСНОВНОМ боте как "Чудо ✨":
--   - Бот находит СУЩЕСТВУЮЩУЮ запись с chat_id=476561547
--   - НЕ создаёт новую запись
--   - Использует старые настройки из тестового бота
--   - Результат: посты НЕ приходят в ЛС
--
-- РЕШЕНИЕ:
-- Для работы в ОСНОВНОМ боте нужны настройки обычного пользователя:
--   dm_enabled=1, channel_enabled=0, channel_id=NULL

-- Исправляем настройки для Оли в основном боте
UPDATE users
SET dm_enabled = 1,
    channel_enabled = 0,
    channel_id = NULL
WHERE chat_id = 476561547;

-- Проверяем результат
SELECT chat_id, username, name, dm_enabled, channel_enabled, channel_id, timezone
FROM users
WHERE chat_id = 476561547;
